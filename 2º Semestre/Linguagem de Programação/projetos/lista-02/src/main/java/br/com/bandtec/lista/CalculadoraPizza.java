    private void btnCalcular01ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcular01ActionPerformed
        // TODO add your handling code here:
        Double valorPizza = Double.valueOf(tfCampo1.getText());
        String saborPizza = String.valueOf(tfCampo2.getText());
        Integer pessoasParaRachar = Integer.valueOf(tfCampo3.getText());
        Double valorMax = Double.valueOf(tfCampo4.getText());
        Double valorPorPessoaPizza = valorPizza/pessoasParaRachar;
        Double deveriaCustar = pessoasParaRachar*valorMax;
        String msg = "";
        
        
        if(valorPorPessoaPizza <= valorMax){
            msg = (String.format("A pizza de %s ser치 dividida entre %d"
                +"pessoas. R$ %.2f para cada", saborPizza, pessoasParaRachar,valorPorPessoaPizza));
        } else {
            msg = (String.format("Deu ruim no racha. A pizza "
                    + "deveria custar no m치ximo R$%.2f" ,deveriaCustar));
        }
        
        lblTexto05.setText(msg);
    }//GEN-LAST:event_btnCalcular01ActionPerformed
    
    
